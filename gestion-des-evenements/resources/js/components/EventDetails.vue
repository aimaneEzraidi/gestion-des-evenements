<template>
    <div class="container">
        <div class="row my-4">
            <div class="col md-8 mx-auto mt-5">
                <div class="card">
                    <h2 class="card-header">{{event.title}}</h2>
                    <p class="p-2">
                        <span class="badge badge-default">
                             {{event.user.name}}
                        </span>
                        <span class="badge badge-info">
                            {{event.created_at}}
                         </span>
                         <span class="badge badge-success">
                            {{event.category}}
                         </span>
                    </p>
                    <div class="card-img-top">
                        <img style="width:100%;height:400px" :src="event.photo" class="rounded shadow-sm img-fluid mr-2" alt="">
                    </div>
                    <div class="card-body">
                        <div class="media">
                            <div class="media-body">
                                <p class="lead">{{event.description.substr(0,300)}}</p>
                            </div>
                        </div>
                    </div>
                </div>
                <div class="row my-3">
                    <div class="col-md-12">
                        <div class="media shadow my-4 p-4" v-if="logged">
                            <div class="media-body">
                                <h3 class="my-3 text-dark">Ajouter un commentaire</h3>
                                <div class="form-group">
                                  <label for="">Commentaire</label>
                                  <textarea 
                                    class="form-control" 
                                    col="30" 
                                    rows="3"
                                    v-model="comment"
                                    ></textarea>
                                </div>
                                <button 
                                type="button" 
                                class="btn btn-success"
                                @click="addComment(event.id)"
                                >Valider</button>
                            </div>
                        </div>
                        <div class="media my-4 shadow p-4" v-else>
                            <div class="media-body">
                                <router-link to="/login ">Connectez vous pour commenter</router-link>
                            </div>
                        </div>
                        <h3 class="text-info mb-3">
                            Commentaires <span class="badge badge-dark p-2">
                                {{event.comments_count}}
                            </span>
                        </h3>
                        <div class="media my-4 p-4 shadow"
                            v-for="(comment,index) in event.comments"
                            :key="index"
                        >
                            <div class="media-body">
                                <span class="text-danger">
                                    {{comment.user}} | {{comment.created_at}}
                                </span>
                                <p class="lead">
                                    {{comment.body.substr(0,100)}}
                                </p>
                            </div>
                        </div>
                    </div>
                </div>
            </div>
        </div>
    </div>
</template>
<script>
export default {
    data(){
        return{
            event :'',
            
            comment: '',
            logged : User.isLogged().logged,
        }
    },
    methods: {
        addComment(id){
            axios.event('/api/comments',{
                event_id: id,
                body: this.comment,
                user_id: User.isLogged().id
            }).then(response => {
                console.log(response.data);
                this.event.comments_coun +=1;
                this.event.comments.unshift(response.data);
                this.comment = null;
            }).catch(err => console.log(err));
        }
    },
    created(){
        axios.get(`/api/events/${this.$route.params.title}`)
        .then(response =>{
            console.log(response.data)
            this.event = response.data;
        }).catch(err => console.log(err));
    }
}
</script>